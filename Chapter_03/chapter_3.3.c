#include <stdio.h>

int main_prog_3_3_1() {
    int x, y;
    scanf("%d %d", &x, &y);
    printf("%d\n", x + y);

    return 0;
}

int main_prog_3_3_2() {
    int x, y;

    scanf("%d %d", &x, &y);

    printf("%d\n", x * 60 + y);
    printf("%d\n", (x * 60 + y) * 60);

    return 0;
}

int main_prog_3_3_3() {
    int x, y;

    scanf("%d %d", &x, &y);

    printf("%d\n", x + 2 * y);

    return 0;
}